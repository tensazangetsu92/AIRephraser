# ЕДИНЫЙ МАКСИМАЛЬНО ЖЕСТКИЙ ПРОМПТ
HUMANIZE_PROMPT_TEMPLATE = """
【ИСХОДНЫЙ ТЕКСТ (КОПИРУЙ ЕГО ПОЛНОСТЬЮ, НИЧЕГО НЕ УДАЛЯЯ)】
{text}

【ТВОЯ ЗАДАЧА】
Перепиши текст выше на {language} более естественным и человеческим языком.

【ПАРАМЕТРЫ ОБРАБОТКИ】
• Сила изменения: {intensity}
• Тон: {tone}
• Стиль: {style}
• Длина: {length}

【ЖЕСТКИЕ ТРЕБОВАНИЯ - НАРУШЕНИЕ ЗАПРЕЩЕНО】
1. ✅ СОХРАНИ ВСЮ ИНФОРМАЦИЮ ДОСЛОВНО - каждое слово, цифру, дату, имя
2. ✅ СОХРАНИ ВСЮ СТРУКТУРУ - все заголовки, подзаголовки, абзацы, списки
3. ✅ СОХРАНИ ВСЕ ЭЛЕМЕНТЫ - номера страниц, сноски, источники, оглавление
4. ❌ НЕ УДАЛЯЙ никакие абзацы, пункты или разделы
5. ❌ НЕ ОБЪЕДИНЯЙ несколько пунктов в один
6. ❌ НЕ УПРОЩАЙ и НЕ КРАТИ текст
7. ❌ НЕ МЕНЯЙ порядок изложения

【ДЛИНА ТЕКСТА - КРИТИЧЕСКИ ВАЖНО】
Исходная длина: {original_length} символов
Твоя задача: сохранить длину {length_instruction}

【ЧТО МОЖНО МЕНЯТЬ】
- Стилистику предложений (делай их более естественными)
- Порядок слов внутри предложений
- Синонимы (если они уместны)
- Падежи и склонения

【ЧТО НЕЛЬЗЯ МЕНЯТЬ】
- Специальные термины и названия
- Цифры, даты, проценты
- Цитаты и источники
- Имена собственные

【ФОРМАТ ОТВЕТА】
ТОЛЬКО готовый текст. НИКАКИХ: "Вот результат", "Я переписал", "---" и т.д.
Начинай ответ ПРЯМО С ПЕРВОЙ СТРОКИ текста.

【ПРОВЕРЬ СЕБЯ ПЕРЕД ОТВЕТОМ】
1. Посчитай абзацы - их столько же, сколько в оригинале?
2. Проверь заголовки - все ли на месте?
3. Сравни длину - она примерно такая же?
4. Все ли цифры и термины сохранились?

ЕСЛИ ТЕКСТ ДЛИННЫЙ (более 5000 символов) - ОБРАБАТЫВАЙ ЕГО ПОСТЕПЕННО, НЕ ТЕРЯЯ ИНФОРМАЦИЮ.
НАЧИНАЙ ОБРАБОТКУ ПРЯМО СЕЙЧАС:
"""


def format_humanize_prompt(text: str, intensity: str, tone: str, style: str, length: str, target_language: str) -> str:
    """Форматирует полный промпт с текстом"""
    original_length = len(text)

    if length == "shorter":
        length_instruction = f"примерно на 20-30% меньше оригинала ({int(original_length * 0.7)}-{int(original_length * 0.8)} символов), НО СОХРАНИ ВСЮ КЛЮЧЕВУЮ ИНФОРМАЦИЮ"
    elif length == "longer":
        length_instruction = f"примерно на 20-30% больше оригинала ({int(original_length * 1.2)}-{int(original_length * 1.3)} символов), добавляй пояснения"
    else:
        length_instruction = f"ПРИМЕРНО ТАКОЙ ЖЕ, КАК ОРИГИНАЛ ({original_length} ±10% символов)"

    return HUMANIZE_PROMPT_TEMPLATE.format(
        text=text,
        language=LANGUAGE_MAP[target_language],
        intensity=INTENSITY_MAP[intensity][target_language],
        tone=TONE_MAP[tone][target_language],
        style=STYLE_MAP[style][target_language],
        length=LENGTH_MAP[length][target_language],
        original_length=original_length,
        length_instruction=length_instruction
    )